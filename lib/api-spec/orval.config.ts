import { defineConfig, InputTransformerFn } from "orval";
import path from "path";

const root = path.resolve(__dirname, "..", "..");
const apiClientReactSrc = path.resolve(root, "lib", "api-client-react", "src");
const apiZodSrc = path.resolve(root, "lib", "api-zod", "src");

// Our exports make assumptions about the title of the API being "Api" (i.e. generated output is `api.ts`).
const titleTransformer: InputTransformerFn = (config) => {
  config.info ??= {};
  config.info.title = "Api";

  return config;
};

export default defineConfig({
  "api-client-react": {
    input: {
      target: "./openapi.yaml",
      // Exclude the binary upload endpoint from the React Query client: Orval's
      // fetch client JSON.stringifies any request body that isn't
      // form-data/url-encoded/text-plain, which corrupts a raw octet-stream
      // upload. The hand-written `useUpload` hook posts the raw bytes instead.
      // The zod target (below) still generates the response schema.
      filters: {
        mode: "exclude",
        // StorageUpload: raw octet-stream upload (hand-written useUpload hook).
        // CostDocumentUpload: raw octet-stream cost-document upload (hand-written hook).
        // billing-pdf: binary PDF download streamed by the server; the frontend
        // opens it via a direct admin link, so no generated hook is needed.
        // The zod target (below) still generates schemas for both.
        tags: ["StorageUpload", "CostDocumentUpload", "billing-pdf"],
      },
      override: {
        transformer: titleTransformer,
      },
    },
    output: {
      workspace: apiClientReactSrc,
      target: "generated",
      client: "react-query",
      mode: "split",
      baseUrl: "/api",
      clean: true,
      prettier: true,
      override: {
        fetch: {
          includeHttpResponseReturnType: false,
        },
        mutator: {
          path: path.resolve(apiClientReactSrc, "custom-fetch.ts"),
          name: "customFetch",
        },
      },
    },
  },
  zod: {
    input: {
      target: "./openapi.yaml",
      override: {
        transformer: titleTransformer,
      },
    },
    output: {
      workspace: apiZodSrc,
      client: "zod",
      target: "generated",
      mode: "split",
      clean: true,
      prettier: true,
      override: {
        zod: {
          coerce: {
            query: ['boolean', 'number', 'string'],
            param: ['boolean', 'number', 'string'],
            body: ['bigint', 'date'],
            response: ['bigint', 'date'],
          },
        },
        useDates: true,
        useBigInt: true,
      },
    },
  },
});
